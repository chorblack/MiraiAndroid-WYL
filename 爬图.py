def detail_page(url):
    headers = {'Accept-Language': 'zh-CN,zh;q=0.9',
               'User-Agent': random.choice(ualist),
               'X-Forwarded-For': random_ip(),
               'referer': 'http://91porn.com',
               'Content-Type': 'multipart/form-data; session_language=cn_CN'}
    session = HTMLSession()
    r = session.get(url, headers=headers)
    try:
        mp4url = r.html.search('<source src="{}" type=\'video/mp4\'>')[0]
        title = r.html.search('<div id="viewvideo-title">{}</div>')[0].strip()
        imgurl = r.html.search('poster="{}"')[0]
        if not os.path.exists(title):
            try:
                os.makedirs(title)
                print('开始下载', title)
                download_img(imgurl, title)
                download_mp4(mp4url, title)
                print(title, '视频和缩略图下载完成')
            except Exception as e:
                traceback.print_exc()
        else:
            print('已存在文件夹{},跳过'.format(title))
    except Exception as e:
        traceback.print_exc()
 
