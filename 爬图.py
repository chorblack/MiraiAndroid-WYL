def random_ip():
    a = str(random.randint(1, 255))
    b = str(random.randint(1, 255))
    c = str(random.randint(1, 255))
    d = str(random.randint(1, 255))
    ipaddress = '{}.{}.{}.{}'.format(a, b, c, d)
    return ipaddress
 
def download_mp4(url, mydir):
    session = HTMLSession()
    headers = {'Accept-Language': 'zh-CN,zh;q=0.9',
               'User-Agent': random.choice(ualist),
               'X-Forwarded-For': random_ip(),
               'referer': 'http://91porn.com',
               'Content-Type': 'multipart/form-data; session_language=cn_CN'}
    req = session.get(url, headers=headers)
    filename = os.path.join(mydir, '1.mp4')
    with open(filename, 'wb') as f:
        f.write(req.content)
 
